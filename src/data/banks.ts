export function getBankById(id: string): Bank | undefined {
  return BANKS.find(b => b.id === id)
}

export function getCardsByBank(bankId: string): Card[] {
  return CARDS.filter(c => c.bank_id === bankId)
}

export function getCardById(id: string): Card | undefined {
  return CARDS.find(c => c.id === id)
}

export function getBenefitsByCard(cardId: string): CardBenefit[] {
  return CARD_BENEFITS.filter(b => b.card_id === cardId)
}

export function getBestCardsForCategory(category: string): Array<Card & { bank: Bank; topBenefit: CardBenefit }> {
  const benefitsInCategory = CARD_BENEFITS.filter(b => b.category === category)
  const cardIds = [...new Set(benefitsInCategory.map(b => b.card_id))]

  return cardIds
    .map(cardId => {
      const card = getCardById(cardId)
      const bank = card ? getBankById(card.bank_id) : undefined
      const benefits = getBenefitsByCard(cardId).filter(b => b.category === category)
      const topBenefit = benefits.sort((a, b) => (b.value ?? 0) - (a.value ?? 0))[0]
      if (!card || !bank || !topBenefit) return null
      return { ...card, bank, topBenefit }
    })
    .filter(Boolean)
    .sort((a, b) => (b!.topBenefit.value ?? 0) - (a!.topBenefit.value ?? 0)) as Array<Card & { bank: Bank; topBenefit: CardBenefit }>
}
